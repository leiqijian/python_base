# ## 题目4：水果折扣计算器
#
# 编写一个程序，让用户输入水果名称、原价和折扣率（如0.9表示9折），然后计算折后价格并输出：
# [水果名称]原价[原价]元，打[折扣率×10]折后价格为[折后价]元。
# 要求：
#
# 1. 获取水果名称、原价和折扣率的输入
# 2. 将价格和折扣率转换为浮点数
# 3. 计算折后价格（原价×折扣率）
# 4. 使用f-string格式化输出，价格保留1位小数
#

name = input("水果名称：")
price = float(input("原价："))
discount = float(input("折扣率："))

calc = round(price*discount,2)

print(f"{name}原价{price}元，打{discount *10}折后价格为{calc}元。")


